    private void SEARCHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SEARCHActionPerformed
        // TODO add your handling code here:
        String selectedYear = SEARCHYEAR.getSelectedItem().toString();
        String selectedMonth = SEARCHMONTH.getSelectedItem().toString();
        
        // Perform database query to fetch transactions based on search criteria
        String query = "SELECT * FROM transactions WHERE Acc_Number = ? AND Year = ? AND Month = ?";
    
        try (Connection conn = new DataBase().getConnection();
        PreparedStatement pstmt = conn.prepareStatement(query)) {
            pstmt.setString(1, accountNumber);
            pstmt.setString(2, selectedYear);
            pstmt.setString(3, selectedMonth);

            ResultSet rs = pstmt.executeQuery();

            // Populate the table with the retrieved data
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0); // Clear existing rows

            while (rs.next()) {
                Object[] row = new Object[]{
                    rs.getInt("Transaction_ID"),
                    rs.getString("Type"),
                    rs.getString("Reason"),
                    rs.getDouble("Amount"),
                    rs.getString("Date"),
                    rs.getString("Month"),
                    rs.getString("Year")
                };
                model.addRow(row);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "An error occurred while fetching transactions.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_SEARCHActionPerformed

