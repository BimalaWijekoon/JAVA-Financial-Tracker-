    private void EDITActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EDITActionPerformed
        // TODO add your handling code here:                                 
        // Retrieve user inputs for editing data
        int transactionId = Integer.parseInt(EDITTRANS.getText());
        String editedType = EDITTYPE.getSelectedItem().toString();
        String editedReason = EDITREASON.getText();
        double editedAmount = Double.parseDouble(EDITAMOUNT.getText());
        String editedDate = EDITDATE.getSelectedItem().toString();
        String editedMonth = EDITMONTH.getSelectedItem().toString();
        String editedYear = EDITYEAR.getSelectedItem().toString();

        // Perform update query to modify the data in the database
        String updateQuery = "UPDATE transactions SET Type = ?, Reason = ?, Amount = ?, Date = ?, Month = ?, Year = ? WHERE Transaction_ID = ? AND Acc_Number = ?";
        
        try (Connection conn = new DataBase().getConnection();
        PreparedStatement pstmt = conn.prepareStatement(updateQuery)) {
            pstmt.setString(1, editedType);
            pstmt.setString(2, editedReason);
            pstmt.setDouble(3, editedAmount);
            pstmt.setString(4, editedDate);
            pstmt.setString(5, editedMonth);
            pstmt.setString(6, editedYear);
            pstmt.setInt(7, transactionId);
            pstmt.setString(8, accountNumber);

            int rowsUpdated = pstmt.executeUpdate();

            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(this, "Transaction updated successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
            
                // Refresh the table to reflect the changes made
                updateTableWithEditedData(transactionId);
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update transaction.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "An error occurred while updating transaction.", "Error", JOptionPane.ERROR_MESSAGE);
        }                                   
    }//GEN-LAST:event_EDITActionPerformed

