    private void updateTableWithEditedData(int transactionId) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    
        // Loop through each row in the table to find the row with the edited transaction ID
        for (int i = 0; i < model.getRowCount(); i++) {
            int id = Integer.parseInt(model.getValueAt(i, 0).toString());
        
            // If the transaction ID matches the edited transaction ID, update the row with edited data
            if (id == transactionId) {
                model.setValueAt(EDITTYPE.getSelectedItem().toString(), i, 1);
                model.setValueAt(EDITREASON.getText(), i, 2);
                model.setValueAt(Double.parseDouble(EDITAMOUNT.getText()), i, 3);
                model.setValueAt(EDITDATE.getSelectedItem().toString(), i, 4);
                model.setValueAt(EDITMONTH.getSelectedItem().toString(), i, 5);
                model.setValueAt(EDITYEAR.getSelectedItem().toString(), i, 6);
                break;
            }
        }
    }
    
