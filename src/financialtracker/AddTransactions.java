    private void SUBMITActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SUBMITActionPerformed
        // TODO add your handling code here:
        // Retrieve data from text boxes and combo boxes
        String selectedYear = YEAR.getSelectedItem().toString();
        String selectedMonth = MONTH.getSelectedItem().toString();
        String selectedDate = date.getSelectedItem().toString();
        String selectedType = TYPE.getSelectedItem().toString();
        String amountText = amount.getText();
        String reasonText = reason.getText();

        // Validate the data
        if (selectedYear.isEmpty() || selectedMonth.isEmpty() || selectedDate.isEmpty() || selectedType.isEmpty() || amountText.isEmpty() || reasonText.isEmpty()) {
            // Show an error message if any field is empty
            JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Convert amount to double
        double transactionAmount = 0.0;
        try {
            transactionAmount = Double.parseDouble(amountText);
        } catch (NumberFormatException e) {
            // Show an error message if amount is not a valid number
            JOptionPane.showMessageDialog(this, "Please enter a valid amount.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Validate other fields as needed

        // Get the user's account number using the username
        int accountNum = Integer.parseInt(accountNumber);
        if (accountNum == 0) {
            // Show an error message if account number is not found
            JOptionPane.showMessageDialog(this, "Account number not found.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Insert the data into the transactions table
        boolean success = insertTransactionData(selectedYear, selectedMonth, selectedDate, selectedType, transactionAmount, reasonText);
        // Update status labels based on insertion result
        if (success) {
            STATUS.setForeground(Color.WHITE);
            STATUS.setText("Successful");
            STATUS.setBackground(Color.GREEN);
            // Update other status labels with entered data
            yearstatus.setText(selectedYear);
            monthstatus.setText(selectedMonth);
            datestatus.setText(selectedDate);
            typestatus.setText(selectedType);
            amountstatus.setText(amountText);
            reasonstatus.setText(reasonText);
        } else {
            STATUS.setText("Failed to add data.");
            STATUS.setForeground(Color.RED);
            // Clear other status labels
            yearstatus.setText("");
            monthstatus.setText("");
            datestatus.setText("");
            typestatus.setText("");
            amountstatus.setText("");
            reasonstatus.setText("");
        }

        // Clear the form after submission
        clearForm();
    }//GEN-LAST:event_SUBMITActionPerformed

