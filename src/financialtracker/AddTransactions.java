    private boolean insertTransactionData(String year, String month, String date, String type, double amount, String reason) {
        // Query to insert data into transactions table
        String insertQuery = "INSERT INTO transactions (Year, Month, Date, Type, Amount, Reason, Acc_Number) VALUES (?, ?, ?, ?, ?, ?, ?)";

        try (Connection conn = new DataBase().getConnection();
            PreparedStatement pstmt = conn.prepareStatement(insertQuery)) {

            // Set parameters for the insert query
            pstmt.setString(1, year);
            pstmt.setString(2, month);
            pstmt.setString(3, date);
            pstmt.setString(4, type);
            pstmt.setDouble(5, amount);
            pstmt.setString(6, reason);
            pstmt.setString(7, accountNumber);

            // Execute the insert query
            int rowsAffected = pstmt.executeUpdate();
            return rowsAffected > 0; // Return true if insertion is successful

        } catch (SQLException e) {
            e.printStackTrace();
            return false; // Return false if insertion fails
        }
    }

